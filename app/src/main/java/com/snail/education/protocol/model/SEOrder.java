public class SEOrder {
    private String body;
    private String money;
    private String sn;
    private String sta;
    private long time;
    private String uid;

    public String getBody() {
        return body;
    }

    public String getMoney() {
        return money;
    }

    public String getSn() {
        return sn;
    }

    public String getSta() {
        return sta;
    }

    public long getTime() {
        return time;
    }

    public String getUid() {
        return uid;
    }
}
